// Operadores de comparação
// == != <= >=

var num1 = 3;
var num2 = 3;
var num3 = 5;

if (num1 == num2) {
  console.log("Comparação de igualdade");
}
if (num3 != num2) {
  console.log("Não é igual");
}
if (num1 <= num3 || num1 <= num2) {
  console.log("Menor ou igualdade");
}
if (num3 >= num1) {
  console.log("Maior ou igualdade");
}
